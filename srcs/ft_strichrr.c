#include "libft.h"

int		ft_strichrr(const char *s, char c, int j)
{
	int		i;


	if (j == 0)
		return (0);
	i = 0;
	while (j > 0 && s[i] != '\0')
	{
		i += 1;
		if (s[i] == c)
			j -= 1;
	}
	printf("ret strichrr: %d\n", (s[i] == c && j == 0) ? (i) : ((s[i] == '\0') ? (-i) : (-1)));
	return ((s[i] == c && j == 0) ? (i) : ((s[i] == '\0') ? (-i) : (-1)));
}
